public class EncapsulationSample {
    private String name;
    private long nanionalCode;
    private int age;
    private String fatherName;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getNanionalCode() {
        return nanionalCode;
    }

    public void setNanionalCode(long nanionalCode) {
        this.nanionalCode = nanionalCode;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getFatherName() {
        return fatherName;
    }

    public void setFatherName(String fatherName) {
        this.fatherName = fatherName;
    }
}
